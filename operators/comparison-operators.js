/**
 * comparison operators (比較運算子)
 * 
 * 
 */

{
  // < (小於), > (大於), <= (小於等於), >= (大於等於)
  var a = 4;
  var b = 10;
  var c = 10;

  if (a>b) {
    console.log('a大於b');
  } else {
    console.log('a小於b'); // a小於b
  }

  if (c >= b) {
    console.log('c大於等於b'); // c大於等於b
  } else {
    console.log('c小於b');
  }
}
let i = 9;

switch (i) {
  case 0:
    console.log('The value is 0.');
    break;
  case 1:
    console.log('The value is 1.');
    break;
  case 2:
    console.log('The value is 2.');
    break;
  case 3:
    console.log('The value is 3.');
    break;
  default:
    console.log('The value is 9');
}
